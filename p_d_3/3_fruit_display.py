# Program to display respective fruit name for 
# the user input of Alphabets(A,B or C)

alphabet = input('Enter the character(A or B or C): ')


# checking for condition
if alphabet == 'A':
    print('Apple')

elif alphabet == 'B':
    print('Banana')
    
elif alphabet == 'C':
    print('Coconut')

else:
    print('INVALID character')